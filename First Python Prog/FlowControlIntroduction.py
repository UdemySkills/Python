#Comparision Operatiors

print("is 4 > 2 ? "+str(4 > 2))
print("is 4 >= 2 ? "+str(4 >= 2))
print("is 4 <= 2 ? "+str(4 <= 2))
print("is 4 != 2 ? "+str(4 != 2))
print("Is Hello = Hello ? "+ str("Hello" == "Hello"))
print("Is Hello = hello ? "+ str("Hello" == "hello"))
print("Is Hello != hello ? "+ str("Hello" != "hello"))
print("is decimal and int comparision allowed ? Yes. 4.0 != 2 ? "+str(4.0 != 2))



